// JS Variables :

// Automatically (not recommended)
// Var (older)
// Let (recommended)
// const (recommended)

//Automatically
x = 2;
document.write(x);
document.write("<br/>");

//var (Older)
var sum1 = 23;
var sum1 = 31;
document.write(sum1 + "<br/>"); //31

// let :- Recommended to use
let SUM1 = 10;
SUM1 = 11;
document.write(SUM1 + "<br/>"); //11

// const
const pi = 3.14;
document.write(pi + "<br/>"); // pi = 3.15; You can't change the value because it's constant
